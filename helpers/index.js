export const formatDate = (date) =>{
    const options = {
        year: 'numeric',
        month: 'short',
        day: 'numeric',
    };
    //Add 1 day to date
    const dateF = new Date(date);
    return dateF.toLocaleDateString('es-ES', options);
}

